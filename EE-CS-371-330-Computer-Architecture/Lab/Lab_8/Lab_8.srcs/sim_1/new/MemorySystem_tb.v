`timescale 1ns / 1ps
// ============================================================
// MemorySystem_tb.v  -  Lab 8 Task 2 Testbench
//
// Manual requirements covered (verified via waveforms):
//   (c1) Write to Data Memory and LEDs
//   (c2) Read  from Data Memory and Switches
//   (c3) Verify the address decoder enables only the correct
//        module for each access
//   (d1) Data Memory stores and outputs correct data
//   (d2) LEDs reflect written values
//   (d3) Switch values are read correctly
//
// No $display or TCL console output - all verification is done
// by inspecting waveforms in Vivado / ModelSim.
//
// Memory map (actual address bit patterns):
//   address[9] == 0              ->  Data Memory  (0x000-0x1FF)
//   address[9:8] == 2'b10        ->  LEDs         (0x200-0x2FF)
//   address[9:8] == 2'b11        ->  Switches     (0x300-0x3FF)
// ============================================================

module MemorySystem_tb;

    // ?? DUT ports ????????????????????????????????????????????
    reg         clk;
    reg         rst;
    reg  [31:0] address;
    reg         readEnable;
    reg         writeEnable;
    reg  [31:0] writeData;
    reg  [15:0] switches;
    wire [31:0] readData;
    wire [15:0] leds;

    // ?? Internal decoder signals - probed for waveform ???????
    wire DataMemWrite     = uut.DataMemWrite;
    wire DataMemRead      = uut.DataMemRead;
    wire LEDWrite         = uut.LEDWrite;
    wire SwitchReadEnable = uut.SwitchReadEnable;

    // ?? DUT instantiation ????????????????????????????????????
    addressDecoderTop uut (
        .clk         (clk),
        .rst         (rst),
        .address     (address),
        .readEnable  (readEnable),
        .writeEnable (writeEnable),
        .writeData   (writeData),
        .switches    (switches),
        .readData    (readData),
        .leds        (leds)
    );

    // ?? Clock - 10 ns period (100 MHz) ???????????????????????
    initial clk = 0;
    always  #5 clk = ~clk;

    // ?? Write task ????????????????????????????????????????????
    // Drives address + writeData for one clock cycle then deasserts.
    task do_write;
        input [31:0] addr;
        input [31:0] data;
        begin
            @(negedge clk);
            address     = addr;
            writeData   = data;
            writeEnable = 1;
            readEnable  = 0;
            @(posedge clk); #1;
            writeEnable = 0;
            address     = 32'd0;
            writeData   = 32'd0;
        end
    endtask

    // ?? Read task ?????????????????????????????????????????????
    // Asserts readEnable for one clock cycle.
    // readData is combinational - valid as soon as address is stable.
    task do_read;
        input [31:0] addr;
        begin
            @(negedge clk);
            address     = addr;
            writeEnable = 0;
            readEnable  = 1;
            @(posedge clk); #1;
            readEnable  = 0;
            address     = 32'd0;
        end
    endtask

    // ?? Bus idle ??????????????????????????????????????????????
    // Holds all bus signals deasserted for N clock cycles.
    // Gives clean separation between test sections in waveform.
    task bus_idle;
        input integer cycles;
        begin
            address     = 32'd0;
            writeEnable = 0;
            readEnable  = 0;
            writeData   = 32'd0;
            repeat(cycles) @(posedge clk);
            #1;
        end
    endtask

    // ????????????????????????????????????????????????????????
    initial begin

        // ?? Initialise all bus signals ????????????????????????
        address     = 32'd0;
        writeEnable = 0;
        readEnable  = 0;
        writeData   = 32'd0;
        switches    = 16'd0;

        // ?? Reset - hold for 4 clock cycles ??????????????????
        rst = 1;
        repeat(4) @(posedge clk); #1;
        rst = 0;
        bus_idle(2);

        // ????????????????????????????????????????????????????
        // SECTION 1 - Write to Data Memory
        //
        // address[9] == 0  ->  DataMemWrite = 1
        //                      LEDWrite     = 0
        //                      SwReadEnable = 0
        //
        // Waveform check:
        //   After each posedge clk where writeEnable=1:
        //     DataMemWrite must be HIGH, all others LOW
        // ????????????????????????????????????????????????????
        do_write(32'h004, 32'hDEADBEEF);   // DataMem[4]   <- 0xDEADBEEF
        bus_idle(1);
        do_write(32'h008, 32'hCAFEBABE);   // DataMem[8]   <- 0xCAFEBABE
        bus_idle(1);
        do_write(32'h064, 32'hAABBCCDD);   // DataMem[100] <- 0xAABBCCDD
        bus_idle(2);

        // ????????????????????????????????????????????????????
        // SECTION 2 - Read from Data Memory
        //
        // address[9] == 0  ->  DataMemRead = 1
        //                      LEDWrite    = 0
        //                      SwReadEnable= 0
        //
        // Waveform check:
        //   address=0x004 -> readData = 0xDEADBEEF
        //   address=0x008 -> readData = 0xCAFEBABE
        //   address=0x064 -> readData = 0xAABBCCDD
        //   address=0x010 -> readData = 0x00000000  (never written)
        // ????????????????????????????????????????????????????
        do_read(32'h004);
        bus_idle(1);
        do_read(32'h008);
        bus_idle(1);
        do_read(32'h064);
        bus_idle(1);
        do_read(32'h010);   // uninitialised location
        bus_idle(2);

        // ????????????????????????????????????????????????????
        // SECTION 3 - Write to LEDs
        //
        // address[9:8] == 2'b10  ->  LEDWrite     = 1
        //                             DataMemWrite = 0
        //                             SwReadEnable = 0
        //
        // Waveform check:
        //   After write to 0x200: leds = 0xAAAA
        //   After write to 0x201: leds = 0x5555
        //   do_read(0x000) -> readData = 0x00000000
        //     (DataMem[0] untouched - proves no cross-write)
        // ????????????????????????????????????????????????????
        do_write(32'h200, 32'h0000AAAA);   // LEDs <- 0xAAAA
        bus_idle(1);
        do_write(32'h201, 32'h00005555);   // LEDs <- 0x5555
        bus_idle(2);
        do_read(32'h000);                  // DataMem[0] must still be 0x00000000
        bus_idle(2);

        // ????????????????????????????????????????????????????
        // SECTION 4 - Read from Switches
        //
        // address[9:8] == 2'b11  ->  SwitchReadEnable = 1
        //                             DataMemRead      = 0
        //                             LEDWrite         = 0
        //
        // leds.v is a 2-stage synchroniser:
        //   switches -> sync_stage1 -> readData[15:0]
        //   Latency = 2 clock cycles. 3 idle cycles used for margin.
        //
        // Waveform check:
        //   switches=0xF0F0, address=0x300 -> readData = 0x0000F0F0
        //   switches=0x0F0F, address=0x300 -> readData = 0x00000F0F
        // ????????????????????????????????????????????????????
        switches = 16'hF0F0;
        bus_idle(3);
        do_read(32'h300);
        bus_idle(1);

        switches = 16'h0F0F;
        bus_idle(3);
        do_read(32'h300);
        bus_idle(2);

        // ????????????????????????????????????????????????????
        // SECTION 5 - Decoder exclusivity
        //
        // Assert both writeEnable and readEnable simultaneously
        // at each address region and observe exactly one enable
        // is active at a time.
        //
        // Waveform check:
        //   address=0x010 (DataMem):        DmW=1 DmR=1 LEDW=0 SwR=0
        //   address=0x200 (LEDs):           DmW=0 DmR=0 LEDW=1 SwR=0
        //   address=0x300 (Switches):       DmW=0 DmR=0 LEDW=0 SwR=1
        //   address=0x1C0 (DataMem upper):  DmW=1 DmR=1 LEDW=0 SwR=0
        // ????????????????????????????????????????????????????
        @(negedge clk);
        address = 32'h010; writeEnable = 1; readEnable = 1;
        writeData = 32'hFFFFFFFF;
        @(posedge clk); #1;
        writeEnable = 0; readEnable = 0;
        bus_idle(1);

        @(negedge clk);
        address = 32'h200; writeEnable = 1; readEnable = 1;
        writeData = 32'hFFFFFFFF;
        @(posedge clk); #1;
        writeEnable = 0; readEnable = 0;
        bus_idle(1);

        @(negedge clk);
        address = 32'h300; writeEnable = 1; readEnable = 1;
        writeData = 32'hFFFFFFFF;
        @(posedge clk); #1;
        writeEnable = 0; readEnable = 0;
        bus_idle(1);

        @(negedge clk);
        address = 32'h1C0; writeEnable = 1; readEnable = 1;
        writeData = 32'hFFFFFFFF;
        @(posedge clk); #1;
        writeEnable = 0; readEnable = 0;
        bus_idle(2);

        // ????????????????????????????????????????????????????
        // SECTION 6 - No cross-contamination
        //
        // Write to DataMem offset 5, then write to LED space at
        // the same lower-bit offset. DataMem[5] must be unchanged.
        //
        // Waveform check:
        //   do_write(0x005, 0xABCD1234) -> DataMem[5] = 0xABCD1234
        //   do_write(0x205, 0xFFFFFFFF) -> leds       = 0xFFFF
        //   do_read(0x005)              -> readData    = 0xABCD1234
        // ????????????????????????????????????????????????????
        do_write(32'h005, 32'hABCD1234);
        bus_idle(1);
        do_write(32'h205, 32'hFFFFFFFF);   // LED write - must not corrupt DataMem[5]
        bus_idle(1);
        do_read(32'h005);                  // readData must still be 0xABCD1234
        bus_idle(3);

        $finish;
    end

    // ?? Timeout watchdog ??????????????????????????????????????
    initial begin
        #20000;
        $finish;
    end

endmodule